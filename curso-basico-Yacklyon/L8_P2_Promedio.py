# Hallar el promedio de tres notas

# Se le pide al usuario que ingrese las tres notas
n1 = float(input('Califiación de la primer materia: '))
n2 = float(input('Califiación de la segunda materia: '))
n3 = float(input('Califiación de la tercer materia: '))

# Se saca el promedio de las tres materias
promedio = (n1+n2+n3)/3

# Se muestra el resultado en pantalla
print('Tu promedio final es de: ' + str(promedio))

