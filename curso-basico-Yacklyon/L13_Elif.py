# Uso de la sentencia Elif

# Se le pide al usuario que ingrese un número
n = int(input('Ingrese un número entero cualquiera: '))
# Se hace la verificación del número para determinar si es positivo, negativo o cero
if n<0:
    print('El número que ingresaste es negativo')
elif n==0:
    print('El número es = 0')
else:
    print('El número es positivo')
    
